const monogoose = require('mongoose')
const Schema = monogoose.Schema

const leaderSchema = new Schema({
    name: {
        type: String,
        required: true,
        unique: true
    },
    image: {
        type: String,
        required: true
    },
    designation: {
        type: String,
        required: true
    },
    abbr: {
        type: String,
        required: true
    },
    description: {
        type: String,
        required: true
    },
    featured: {
        type: Boolean,
        default: false
    }
})

const Leader = monogoose.model('Leader', leaderSchema)

module.exports = Leader